            <h2 class="text-2xl md:text-3xl font-extrabold text-gray-900 dark:text-white mt-8 mb-4 tracking-tight">Choosing the Right Cable Category</h2>

            <p><strong>Cat6:</strong> Supports 1 Gbps at full 100 meters and 10 Gbps at distances under 55 meters. Suitable for small office deployments and basic connectivity where 10G is not an immediate requirement.</p>

            <p><strong>Cat6A:</strong> Supports 10 Gbps at full 100 meters and handles PoE++ (up to 90W) for 802.3bt devices like 4K displays and building access panels. <strong>This is the minimum recommendation for all new enterprise installations in Dubai.</strong></p>

            <p><strong>Cat7:</strong> Offers improved shielding for high-interference environments such as factories and data centers. Uses GG45 connectors and maintains strict EMI isolation.</p>

            <p><strong>Single-Mode Fiber (OS2):</strong> Supports 100+ Gbps over distances of 10km and beyond. The standard choice for core backbone connectivity between floors or across campus buildings.</p>

            <div class="bg-blue-50 dark:bg-blue-900/10 border-l-4 border-blue-600 p-6 rounded-r-2xl my-6">
