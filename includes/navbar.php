<script>
    (function () {
        const btn = document.getElementById('mobile-menu-btn');
        const drawer = document.getElementById('nav-drawer');
        const overlay = document.getElementById('drawer-overlay');
        const panes = document.getElementById('drawer-panes');
        const categoryIcon = document.getElementById('category-icon');
        const closeIcon = document.getElementById('close-icon');

        // Nav Logic
        const openSolutions = document.getElementById('open-solutions');
        const backToMain = document.getElementById('back-to-main');
        const drawerClose = document.getElementById('drawer-close');

        function openDrawer() {
            drawer.classList.remove('-translate-x-full');
            drawer.classList.add('shadow-active');
            overlay.classList.remove('hidden');
            setTimeout(() => overlay.classList.add('opacity-100'), 10);
            categoryIcon.classList.add('active');
            setTimeout(() => {
                categoryIcon.classList.add('hidden');
                closeIcon.classList.remove('hidden');
            }, 200);
            document.body.style.overflow = 'hidden';
        }

        function closeDrawer() {
            drawer.classList.add('-translate-x-full');
            drawer.classList.remove('shadow-active');
            overlay.classList.remove('opacity-100');
            panes.classList.remove('-translate-x-1/2'); // Reset scroll position
            categoryIcon.classList.remove('hidden');
            categoryIcon.classList.remove('active');
            closeIcon.classList.add('hidden');
            setTimeout(() => overlay.classList.add('hidden'), 300);
            document.body.style.overflow = '';
        }

        btn.addEventListener('click', openDrawer);
        drawerClose.addEventListener('click', closeDrawer);
        overlay.addEventListener('click', closeDrawer);

        // Sub-menu Logic
        openSolutions.addEventListener('click', () => {
            panes.classList.add('-translate-x-1/2');
        });

        backToMain.addEventListener('click', () => {
            panes.classList.remove('-translate-x-1/2');
        });

        // Click-to-close on links
        const navLinks = drawer.querySelectorAll('a');
        navLinks.forEach(link => {
            link.addEventListener('click', () => {
                closeDrawer();
            });
        });
    })();

    // Theme toggle
    (function () {
        var toggle = document.getElementById('theme-toggle');
        toggle.addEventListener('click', function () {
            var html = document.documentElement;
            if (html.classList.contains('dark')) {
                html.classList.remove('dark');
                localStorage.setItem('theme', 'light');
            } else {
                html.classList.add('dark');
                localStorage.setItem('theme', 'dark');
            }
        });
    })();
</script>